# Index for fast lookup
_TOPICS_BY_SLUG = {t.slug: t for t in HELP_TOPICS}


# ---- Endpoints ----


@router.get("/topics", response_model=List[HelpTopicSummary])
async def list_help_topics():
    """Returns a list of all available help topics (slug + title)."""
    return [
        HelpTopicSummary(slug=t.slug, title=t.title) for t in HELP_TOPICS
    ]


@router.get("/topics/{slug}", response_model=HelpTopic)
async def get_help_topic(slug: str):
    """Returns full help content for a specific topic by slug."""
    topic = _TOPICS_BY_SLUG.get(slug)
    if not topic:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail=f"Тема справки '{slug}' не найдена",
        )
    return topic
